    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        $product = Products::find($id);
        $custom_fields = json_decode($product->custom_fields, TRUE);
        $custom_size = json_decode($product->custom_size, TRUE);
        $custom_color = json_decode($product->custom_color, TRUE);

        
        return view('erp.products.show')->with(compact('product', 'custom_fields', 'custom_size', 'custom_color'));
    }

